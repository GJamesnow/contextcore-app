def get_google_data(address):
    """Fetches data and builds the static map URL."""
    if not GOOGLE_API_KEY: return None, "Missing API Key"
    
    # Geocode
    try:
        geo = requests.get(f"https://maps.googleapis.com/maps/api/geocode/json?address={address}&key={GOOGLE_API_KEY}").json()
        if not geo.get('results'): return None, "Address not found"
        loc = geo['results'][0]['geometry']['location']
        lat, lng = loc['lat'], loc['lng']
        fmt_addr = geo['results'][0]['formatted_address']
    except: return None, "Google Connection Failed"

    # Places Scan
    places = requests.get(f"https://maps.googleapis.com/maps/api/place/nearbysearch/json?location={lat},{lng}&radius=1500&type=store&keyword=coffee|grocery|fitness&key={GOOGLE_API_KEY}").json()
    
    competitors = []
    markers = []
    
    for p in places.get('results', [])[:8]:
        name = p.get('name')
        types = p.get('types', [])
        ctype = "Retail"
        if "cafe" in types or "coffee" in name.lower(): ctype = "Coffee"
        elif "grocery" in types or "supermarket" in types: ctype = "Grocery"
        elif "gym" in types or "fitness" in types: ctype = "Fitness"
        
        competitors.append({"name": name, "type": ctype})
        markers.append(f"color:red%7Csize:small%7C{p['geometry']['location']['lat']},{p['geometry']['location']['lng']}")

    # Static Map URL
    marker_str = f"&markers=color:blue%7Csize:mid%7C{lat},{lng}"
    if markers: marker_str += "&markers=" + "&markers=".join(markers)
    static_map_url = f"https://maps.googleapis.com/maps/api/staticmap?center={lat},{lng}&zoom=14&size=640x350&maptype=roadmap{marker_str}&key={GOOGLE_API_KEY}"

    # Logic
    score = 60 + (len(competitors) * 2)
    score_color = "#28a745" if score > 80 else "#ffc107"
    status = "High Demand" if score > 80 else "Stable"
    void_count = 3 - len(set([c['type'] for c in competitors if c['type'] in ['Coffee','Grocery','Fitness']]))

    return {
        "address": fmt_addr,
        "score": score,
        "score_color": score_color,
        "status": status,
        "void_count": max(0, void_count),
        "static_map_url": static_map_url,
        "competitors": competitors,
        "lat": lat, "lng": lng
    }, None

